num1 = float(input("Insira um numero: \n"))
num2 = float(input("Insira mais um numero: \n"))

soma = num1 + num2

if soma > 10:
    print("O resultado da adição de seus numeros foi ", soma, ", consequentemente, foi maior do que 10.")

else:
    print("O resultado da adição de seus numeros nao foi maoir do que 10.")